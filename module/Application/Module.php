namespace Application;

use Application\Model\Categoria;
use Application\Model\CategoriaTable;
use Application\Model\Produto;
use Application\Model\ProdutoTable;
use Zend\Db\ResultSet\ResultSet;
use Zend\Db\TableGateway\TableGateway;
use Zend\Mvc\ModuleRouteListener;
use Zend\Mvc\MvcEvent;

class Module
{
    public function onBootstrap(MvcEvent $e)
    {
        $eventManager        = $e->getApplication()->getEventManager();
        $moduleRouteListener = new ModuleRouteListener();
        $moduleRouteListener->attach($eventManager);
    }

    public function getConfig()
    {
        return include __DIR__ . '/config/module.config.php';
    }

    public function getAutoloaderConfig()
    {
        return array(
            'Zend\Loader\StandardAutoloader' => array(
                'namespaces' => array(
                    __NAMESPACE__ => __DIR__ . '/src/' . __NAMESPACE__,
                ),
            ),
        );
    }
    
    public function getServiceConfig()
    {
        return array(
            'factories' => array(
                'Application\Model\CategoriaTable' => function($sm){
                    $tableGateway = $sm->get('CategoriaTableGateway');
                    return new CategoriaTable($tableGateway);
                },
                'CategoriaTableGateway' => function($sm){
                    $dbAdapter = $sm->get('Zend\Db\Adapter\Adapter');
                    $resultSetPrototype = new ResultSet();
                    $resultSetPrototype->setArrayObjectPrototype(new Categoria());
                    return new TableGateway('tb_categoria_produto', $dbAdapter, null, $resultSetPrototype);
                },
                'Application\Model\ProdutoTable' => function($sm){
                    $tableGateway = $sm->get('ProdutoTableGateway');
                    return new ProdutoTable($tableGateway);
                },
                'ProdutoTableGateway' => function($sm){
                    $dbAdapter = $sm->get('Zend\Db\Adapter\Adapter');
                    $resultSetPrototype = new ResultSet();
                    $resultSetPrototype->setArrayObjectPrototype(new Produto());
                    return new TableGateway('tb_produto', $dbAdapter, null, $resultSetPrototype);
                }
            )
        );
    }
}
